mu_em = np.array([-1.0, 1.0])   # init far from truth
sig_em = np.array([1.0, 1.0])

for iteration in range(20):
    # E-step: compute responsibilities
    r = np.column_stack([
        pi_em[k] * stats.norm.pdf(X_em, mu_em[k], sig_em[k])
        for k in range(2)
    ])
    r /= r.sum(axis=1, keepdims=True)
    # M-step: update parameters
    N_k = r.sum(0)
    pi_em  = N_k / len(X_em)
    mu_em  = (r * X_em[:,None]).sum(0) / N_k
    sig_em = np.sqrt(((r * (X_em[:,None]-mu_em)**2).sum(0)) / N_k)

print(f"  EM GMM converged: π={pi_em.round(3)}  μ={mu_em.round(3)}  σ={sig_em.round(3)}")


