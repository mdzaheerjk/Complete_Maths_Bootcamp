class VAE(nn.Module):
    def __init__(self, input_dim=20, latent_dim=4):
        super().__init__()
        self.encoder = nn.Sequential(nn.Linear(input_dim, 16), nn.ReLU())
        self.fc_mu     = nn.Linear(16, latent_dim)
        self.fc_logvar = nn.Linear(16, latent_dim)
        self.decoder = nn.Sequential(
            nn.Linear(latent_dim, 16), nn.ReLU(),
            nn.Linear(16, input_dim), nn.Sigmoid())

    def reparameterize(self, mu, logvar):
        # z = μ + σ·ε,  ε~N(0,I)  — reparameterization trick
        eps = torch.randn_like(mu)
        return mu + torch.exp(0.5*logvar) * eps

    def forward(self, x):
        h      = self.encoder(x)
        mu_enc = self.fc_mu(h)
        logvar = self.fc_logvar(h)
        z      = self.reparameterize(mu_enc, logvar)
        x_rec  = self.decoder(z)
        return x_rec, mu_enc, logvar

def vae_loss(x_rec, x, mu, logvar):
    # ELBO = Reconstruction - KL(q||p)
    # Reconstruction: binary cross-entropy
    recon = nn.BCELoss(reduction='sum')(x_rec, x)
    # KL: D_KL(N(μ,σ²)||N(0,I)) = 0.5·Σ(μ²+σ²-logσ²-1)
    kl    = -0.5 * torch.sum(1 + logvar - mu**2 - torch.exp(logvar))
    return recon + kl, recon, kl

