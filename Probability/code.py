def rbf_kernel(X1, X2, sigma=1.0, length_scale=1.0):
    """k(x,x') = σ²·exp(-||x-x'||²/2ℓ²)"""
    sq_dist = np.sum((X1[:,None] - X2[None,:])**2, axis=-1)
