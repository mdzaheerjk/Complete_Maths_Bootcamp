sub("5.2 Pairwise ≠ Mutual Independence (Counterexample)")
# Three events: all pairwise independent, but NOT mutually independent
# Ω = {HHH, HHT, HTH, HTT, THH, THT, TTH, TTT}
# A = {HHH,HHT,HTH,HTT}, B = {HHH,HHT,THH,THT}, C = {HHH,HTH,THH,TTH}
outcomes = list(itertools.product('HT', repeat=3))
def check_ind(ev_a, ev_b, omega):
    p_a = len(ev_a) / len(omega)
    p_b = len(ev_b) / len(omega)
    p_ab = len(ev_a & ev_b) / len(omega)
    return abs(p_a * p_b - p_ab) < 1e-9

omega_set = set(outcomes)
A3 = {o for o in outcomes if o[0] == 'H'}   # first is H
B3 = {o for o in outcomes if o[1] == 'H'}   # second is H
C3 = {o for o in outcomes if o[0] == o[1]}  # first == second

pairwise = all([check_ind(set(A3),set(B3),outcomes),
                check_ind(set(A3),set(C3),outcomes),
                check_ind(set(B3),set(C3),outcomes)])
p_abc  = len(set(A3) & set(B3) & set(C3)) / len(outcomes)
mutual = abs(p_abc - 0.5**3) < 1e-9
print(f"  Pairwise independent: {pairwise}")
print(f"  Mutually independent: {mutual}  (P(A∩B∩C)={p_abc:.4f} vs 0.125) — counterexample ✓")

